// we have 5 primitive data types in JavaScript
// - number - R (real numbers)
// - string - words, sentences, characters, empty string
// - boolean - true or false
// - undefined
// - null

let space = " ";
let emptyString = "";
console.log(`"${space}"`);
console.log(`Length of space variable is: ${space.length}`);
console.log(`Data type of space variable is: ${typeof space}`);
console.log(`Data type of emptyString variable is: ${typeof emptyString}`);

