// = - is an assignment operator
let age = 23;
let firstName = "John";

// comparison operators
console.log(`age == 27 = ${age == 27} and it's data type is ${typeof (age == 27)}`);
console.log(`age > 18: ${age > 18}`);
console.log(`age < 18: ${age < 18}`);
console.log(`age >= 23: ${age >= 23}`);
console.log(`age <= 23: ${age <= 23}`);
console.log(`age != 23: ${age != 23}`);

console.log(age + 2);

// Logical operators
// && - AND operator
console.log(age > 18 && age < 30);

// || - OR operator
console.log(age < 18 || age > 30);
console.log(5 ** 2 > 4 ** 3);

// ! - NOT operator
console.log(!(age > 18));

console.log(false && false);
console.log(18 > 17 && 18 < 5);

